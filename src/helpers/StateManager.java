	public static void update() {
		switch(gameState) {
		case MAINMENU:
			if (mainMenu == null)
				mainMenu = new MainMenu();
			mainMenu.update();
			break;
			
		case EDITOR:
			if (editor == null)
				editor = new Editor(map);
			
			mainMenu.sidebarUpdate(editor.getTileTypeSelection());
			editor.update();
			break;
		}
	}
	
	public static void setState(GameState newState) {
		gameState = newState;
	}
